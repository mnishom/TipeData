package tipedata;

public class Methods {
    
    public static double volume_kubus(double sisi){
        return sisi * sisi * sisi; 
        //return Math.pow(sisi, 3);
    }
    public static double keliling_kubus(double sisi){
        return 12 * sisi * sisi;
    }    
    public static double luas_kubus(double sisi){
        return 6 * sisi * sisi;
    } 
}
